-- Khởi tạo database TRUONGHOC
CREATE DATABASE TRUONGHOC;

-- Sử dụng database
USE TRUONGHOC;

-- Tạo bảng HOCSINH
CREATE TABLE HOCSINH
(
	MAHS CHAR(5),
	TEN NVARCHAR(30),
	NAM BOOLEAN, -- MySQL thường dùng BOOLEAN (thực chất là TINYINT) thay vì BIT cho giới tính
	NGAYSINH DATETIME,
	DIACHI VARCHAR(20),
	DIEMTB FLOAT
);

-- Tạo bảng GIAOVIEN
CREATE TABLE GIAOVIEN
(
	MAGV CHAR(5),
	TEN NVARCHAR(30),
	NAM BOOLEAN, 
	NGAYSINH DATETIME,
	DIACHI VARCHAR(20),
	LUONG DECIMAL(15,2) -- LƯU Ý: MySQL KHÔNG CÓ kiểu MONEY. Ta phải dùng DECIMAL(15,2) để lưu tiền tệ.
);

-- Tạo bảng LOPHOC
CREATE TABLE LOPHOC
(
	MALOP CHAR(5),
	TENLOP NVARCHAR(30),
	SOLUONG INT
);
